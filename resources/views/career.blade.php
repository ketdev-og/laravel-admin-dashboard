					<p>Key focus areas will be: analyzing profitability of new business opportunities and achieving fee income and solidifying customer retention efforts through superior customer service; developing and implementing a strategic roadmap for the Bank’s treasury and digital banking products/services for a seamless customer experience across all digital channels; implementing and maintaining treasury and digital banking services/products to ensure regulatory risk assessment and compliance; developing, enhancing and supporting the consumer mobile and online banking products and services to include payments and remote deposit capture; leading, participating and collaborating with CAO and executive management on various strategic technology projects emphasizing customer service, targeting customer needs and operational efficiencies while ensuring appropriate risk framework, governance and monitoring for listed operations to ensure full compliance with regulations and internal policies and procedures. </p>

