					<h1>Enhanced Security for your Debit Card</h1>
					<p>Saxo Investment Bank debit cards have chip technology. The embedded computer chip provides more security and wider international acceptance.</p>
					<p>A microchip embedded in the card adds a layer of protection against fraud for purchases made at the point of sale. Information programmed into the chip is personalized for your account and each transaction generates a unique code, adding an extra safeguard against unauthorized use and counterfeiting.</p>
					<p>These cards are already in wide use around the world and are now becoming the standard in the U.S.</p>
					
					<a href="contact">Learn More</a>

					<h1>Fraud Monitoring</h1>
					<p>To protect your account, Saxo Investment Bank partners with a third party to monitor your ATM and debit card transactions for potentially fraudulent activity.  If your transactions are suspected to be fraudulent, you will receive a call to validate the legitimacy of your transactions.</p>
					<p>If we suspect fraudulent ATM or debit card use, the following will occur:</p>

					<ul>
						<li><span>&hearts;</span>&nbsp;&nbsp;Our automated call will ask you to verify recent transaction activity on your card</li>
						<li><span>&hearts;</span>&nbsp;&nbsp;You’ll be able to respond via your touchtone keypad</li>
						<li><span>&hearts;</span>&nbsp;&nbsp;Mobile Banking and Mobile Check Deposit</li>
						<li><span>&hearts;</span>&nbsp;&nbsp;If you are unable to be reached, a voicemail will be left with a callback number</li>
						<li><span>&hearts;</span>&nbsp;&nbsp;You will never be asked to provide your account number, social security number, or any other personal information</li>
					</ul>

					<p style="margin-top:4%;">Some of the transaction activities which may prompt a call include:</p>

					<ul>
						<li><span>&hearts;</span>&nbsp;&nbsp;A sudden change in the locale of transactions</li>
						<li><span>&hearts;</span>&nbsp;&nbsp;A sudden string of costly purchases</li>
						<li><span>&hearts;</span>&nbsp;&nbsp;Mobile Banking and Mobile Check Deposit</li>
						<li><span>&hearts;</span>&nbsp;&nbsp;Any pattern associated with new fraud trends around the world</li>
						<li><span>&hearts;</span>&nbsp;&nbsp;Your response to the call is critical to prevent potential fraud and avoid restrictions that may be placed on your card. To ensure we can continue to reach you, please keep us informed of your correct phone number and address at all times</li>
					</ul>

